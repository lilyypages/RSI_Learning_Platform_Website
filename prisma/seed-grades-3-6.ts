async function main() {
  const classes = await prisma.class.findMany({ orderBy: { gradeLevel: "asc" } });
  const subjects = await prisma.subject.findMany();

  function findCs(clsId: string, code: string) {
    return prisma.classSubject.findFirst({ where: { classId: clsId, subjectId: subjects.find(s => s.code === code)!.id } });
  }

  const allGrades: { grade: number; mats: ReturnType<typeof M>[] }[] = [
    { grade: 3, mats: genGrade3() },
    { grade: 4, mats: genGrade4() },
    { grade: 5, mats: genGrade4().map(m => ({ ...m, title: m.title + " (lanjutan)" })) },
    { grade: 6, mats: GRADE6 },
  ];

  for (const g of allGrades) {
    const cls = classes.find(c => c.gradeLevel === g.grade);
    if (!cls) { console.log(`Kelas ${g.grade} tidak ditemukan, skip`); continue; }
    for (let i = 0; i < g.mats.length; i++) {
      const def = g.mats[i];
      const cs = await findCs(cls.id, def.cs);
      if (!cs) continue;
      const mat = await prisma.material.create({
        data: { title: def.title, classSubjectId: cs.id, difficulty: def.diff, isPublished: true, orderIndex: i + 1 },
      });
      for (const q of def.qs) {
        await prisma.question.create({
          data: { materialId: mat.id, questionText: q.q, options: q.o, correctAnswer: q.a, difficulty: q.d, orderIndex: def.qs.indexOf(q) + 1 },
        });
      }
      console.log(`  ${cls.name} ${def.cs} - ${def.title}: ${def.qs.length} soal`);
    }
  }

  console.log("\nGrades 3-6 seeded!");
}

main().catch(console.error).finally(() => prisma.$disconnect());
